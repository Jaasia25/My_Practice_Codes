
package Operators;

public class Assignment 
{
    public static void main(String[] args)
    {
        int a= 4, b= 5;
        
        a+=b; //a=a+b=9
        System.out.println(a);
        
        a-=b; //a=a-b=4
        System.out.println(a);
        
        a*=b; //a=a*b=20
        System.out.println(a);
        
        a/=b; //a=a/b=4
        System.out.println(a);
    }
}
